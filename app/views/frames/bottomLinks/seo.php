<center>
	<h1>SEO, what killed frames</h1>
</center>
<p>
	The main thing that killed frames were lack of SEO.  Google and Yahoo's crawler could not index a frame based
	web page.  If the noframes tag was not used, then the only thing that was displayed on the search was "Your 
	browser does not support frames."  
</p>
<p>
	Initially, Yahoo used a "yellowbook" style of page indexing, which required a user to register their website 
	in a particular category, then sub-category of the category, and so on.  To get higher views, the web page 
	owner would have to pay Yahoo to index them higher.
</p>
<p>
	Google made a "crawler" that scanned webpages for certain key words, meta attributes, and terms.  Initially, it 
	was rife with abuse, but Google continually updated their search algorithm to get rid of people gaming the system.  Those people earned the term "Black-hat SEO" for their tactics like keyword stuffing or stuffing the meta tag with attributes.  Since the crawler couldn't parse frame-based layouts, the only options were to either 
	create lots of noframe attributes or to choose an alternative layout style.
</p>