<p>
	Another benefit of frames was the ability to use HTML without a backend framework to improve modularity.  
	In these code examples, I am running PHP to include files and folders into seperate directories.  This makes 
	working with a SPA much easier than writing all the code inline.  There are difficulties in modularizing 
	external components like CSS and JavaScript, but very little JavaScript or CSS existed on the web at this time.  
	Support between browsers was very inconsistent.
</p>
<p>
	Once a frame is defined, that frame persists for as long as the parent page contains it.  In contrast, most 
	web pages not using a framework have to repeat the headers, footers, and navigation elements 
	in the code, making changes to it difficult.  DreamWeaver became popular because of its ability to 
	create template-layouts and make persistent changes to repeating elements in one file. 
</p>