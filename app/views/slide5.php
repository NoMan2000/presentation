	<section>
		<h3>
			2009 - 2013:  Mobile First
		</h3>
		<ul>
			<li>Tremendous success of Apple's Iphone pushed web developers to change their context.
				Instead of thinking of the web as a print page, the focus shifted to mobile apps.
			</li>
			<li>
				Raster graphics were replaced more by icon-fonts, SVGs, and other vector-based, infinitely 
				scalable formats.
			</li>
			<li>
				Elements that required plug-ins became natively supported, like video, audio, and other 
				elements.
			</li>
			<li>
				Design switched to a "Flat" UI look that emphasized large, flat colors and easy to touch 
				icons and buttons.  The goal was to remove visual "clutter" and distractions.
			</li>
		</ul>
	</section>
	<section>
		<h3>Problem</h3>
		<p>
			The biggest problem developers faced were speed implementations.  "Native" code ran much faster than 
			HTML.
		</p>
		<p>
			Limited local storage another major limitation.  This also pushed developers away from raster graphics, 
			as well as difficulty in dealing with the load times over slower mobile network connections.
		</p>
		<p>
			Partially solved by moving towards GPU-accelerated transitions with CSS, shifting towards "micro-libraries"
			of javaScript that were small and specialized, frameworks designed around mobile-experience, and 
			techniques like concatenation of code, minification of assets, serving special assets to mobile clients,
			and only loading assets when they were needed.
		</p>
		<p>
			Also partially solved because the speed of processors and power of the RAM in phones keeps going up, as well as
			browser manufacturers continually striving for faster speed in code execution against their competitors. As well, 
			wireless companies upgraded their networks to serve content faster.
		</p>
	</section>
</section>