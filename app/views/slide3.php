	<h3>
		2000-2005:  Web as Print
	</h3>
	<p>
	In the net phase of web design, the web was viewed as a surrogate for print design.  Layouts were designed in 
	tables, which were then inline-styled using html tags.  Web pages tended to load slowly, and were often 
	just photoshop files cut up so little pieces of text or other elements could be reused.  
	</p>
	<p>
		Macromedia offered a suite of products that incorporated into this workflow with DreamWeaver, Flash, and 
		Fireworks creating a full workflow for creating webpages.
	</p>
	<p>
		An example of <a href="views/tables/index.html" target='_blank'>Table-based layout</a>
	</p>
	</section>
	<section>
		<h3>Upsides:</h3>
		<ul><li>Better Layout and Organization of Content</li>
			<li>Better visual presentation</li>
			<li>More SEO-friendly</li>
			<li>No "Orphaned" users</li>
		</ul>
	</section>
	<section>
		<h3>Downsides:</h3>
		<ul><li>"Static" content.</li>
		<li>Content often took long load times.</li>
		<li>Difficult to update, often requiring the designer to resubmit work.
		<br/>Sidenote:  Great time to have been a designer.
		</li>
		<li>Using CGI (Common Gateway Interface) often relied upon very insecure PERL or C programs to 
		deliver email.  High volumes of spam.</li>
		<li>Hacked websites often delivered spam or viruses, a common Windows experience.</li>
		</ul>
	</section>
